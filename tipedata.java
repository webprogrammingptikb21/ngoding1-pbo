public class tipedata {
    public static void main (String[] args) {
        byte myNumbyte = 100; //tipe data byte
        short myNumshort = 5000; // pendeklarasian tipe data short
        int myNumint = 100000; //pendeklarasian tipe data int
        long myNumlong = 15000000000L; //tipe data long
        float myNumFloat = 5.75f; //tipedata float diakhiri dengan huruf f
        double myNumdouble = 19.99d; //pendeklarasian tipe data double nilai yang akan disimpan pada variable yang hruf d

        System.out.println(myNumbyte);
        System.out.println(myNumshort);
        System.out.println(myNumint); // 10-15 itu untuk menampilkan isi dari setiap variable
        System.out.println(myNumlong);
        System.out.println(myNumFloat);
        System.out.println(myNumdouble);
    }
}
